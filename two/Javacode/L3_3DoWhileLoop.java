package Git.two.Javacode;

public class L3_3DoWhileLoop {

    public static void main(String[] args) {
        int i = 1; // Initialize the variable i to 1

        do {
            // The body of the do-while loop starts here
            System.out.println("Hi" + i); // Print "Hi" followed by the current value of i
            i++; // Increment the value of i by 1
        } while (i <= 4); // The loop continues as long as i is less than or equal to 4
    }
}
// This program prints "Hi" followed by the current value of i, and then increments i by 1, until i is greater than 4.
